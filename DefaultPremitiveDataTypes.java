package dayOne;

public class DefaultPremitiveDataTypes {
	static boolean ooleanDefault;
	   static double  oubleDefault;
	   static float  loatDefault;
	   static int  ntDefault;
	   static long  ongDefault;
	   static byte yteDefault;
	   static short hortDefault;
	   static char 	harDefault;
	   
public static void main(String[] args) {
	      System.out.println("Default values for premitive datatypes are following");
	      System.out.println("Boolean = " + ooleanDefault);
	      System.out.println("Double = " + oubleDefault);
	      System.out.println("Float = " + loatDefault);
	      System.out.println("Integer = " + ntDefault);
	      System.out.println("Long = " + ongDefault);
	      System.out.println("Byte = " + yteDefault);
	      System.out.println("Short = " + hortDefault);
	      System.out.println("Character = " + harDefault);
	      
	   }
	}